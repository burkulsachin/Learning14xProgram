package ex_03_Operators;

public class Lab0037_Ralational_Operator {
    public static void main(String[] args) {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        int a = 10;
        int b = 30;
        boolean c = a > b; // // 10> 30
        System.out.println(c);

        int age_mamitha = 33;
        int age_sachin = 34;

        boolean result = age_sachin >= age_mamitha;
        System.out.println(result);
    }
}
